package jptflog.model.question;

public class ChoiceTotVO {
	private int Atot;
	private int Btot;
	private int Ctot;
	private int Dtot;
	
	private int responses;
	
	public int getResponses() {
		return responses;
	}
	public void setResponses(int responses) {
		this.responses = responses;
	}
	public int getAtot() {
		return Atot;
	}
	public void setAtot(int atot) {
		Atot = atot;
	}
	public int getBtot() {
		return Btot;
	}
	public void setBtot(int btot) {
		Btot = btot;
	}
	public int getCtot() {
		return Ctot;
	}
	public void setCtot(int ctot) {
		Ctot = ctot;
	}
	public int getDtot() {
		return Dtot;
	}
	public void setDtot(int dtot) {
		Dtot = dtot;
	}
	
}
